package jwrmodel;

import java.util.Date;

public class Str_PRAction {
	
	String body;
	String participantInstId;
	Date actionDate;
	String type;
	String instId;
	
	public Str_PRAction()
	{
		
	}

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getParticipantInstId() {
		return participantInstId;
	}

	public void setParticipantInstId(String participantInstId) {
		this.participantInstId = participantInstId;
	}

	public Date getActionDate() {
		return actionDate;
	}

	public void setActionDate(Date actionDate) {
		this.actionDate = actionDate;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getInstId() {
		return instId;
	}

	public void setInstId(String instId) {
		this.instId = instId;
	}
	
	

}
